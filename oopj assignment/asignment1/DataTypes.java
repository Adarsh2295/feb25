class DataTypes{
	public static void main(String args[]){
	boolean b = true;
	byte by = 5;
	short s = 10;
	int i = 12;
	long l = 1000;
	char c = 'A';
	float f = 10.23f;
	double d = 10.2234d;
	System.out.println(b);
	System.out.println(by);
	System.out.println(s);
	System.out.println(i);
	System.out.println(l);
	System.out.println(c);
	System.out.println(f);
	System.out.println(d);
	
	}


}